import dotenv from 'dotenv';
import app from './app';
import { initWebSocket } from './utils/websocketInstance';

// Load environment variables
dotenv.config(); // Trigger restart
const PORT = parseInt(process.env.PORT || '3001', 10);

// Check database connection state
const mongoose = require('mongoose');

// Start server
const server = app.listen(PORT, '0.0.0.0', () => {
  console.log(`ðŸš€ Server running in ${process.env.NODE_ENV || 'development'} mode on port ${PORT}`);
});

// Initialize WebSocketService
initWebSocket(server);

// Handle server errors
server.on('error', (error: NodeJS.ErrnoException) => {
  if (error.syscall !== 'listen') {
    throw error;
  }

  const bind = typeof PORT === 'string' ? 'Pipe ' + PORT : 'Port ' + PORT;

  switch (error.code) {
    case 'EACCES':
      console.error(`âŒ ${bind} requires elevated privileges`);
      process.exit(1);
      break;
    case 'EADDRINUSE':
      console.error(`âŒ ${bind} is already in use`);
      process.exit(1);
      break;
    default:
      throw error;
  }
});

// Graceful shutdown
const gracefulShutdown = (signal: string) => {
  console.log(`\nðŸ›‘ ${signal} received. Starting graceful shutdown...`);
  
  server.close((err) => {
    if (err) {
      console.error('âŒ Error during server shutdown:', err);
      process.exit(1);
    }
    
    console.log('âœ… Server closed successfully');
    
    // Close database connection
    // Note: mongoose.connection.close() is handled in the database config
    
    console.log('ðŸ‘‹ Graceful shutdown completed');
    process.exit(0);
  });
  
  // Force close after 10 seconds
  setTimeout(() => {
    console.error('â° Forcing shutdown after 10 seconds...');
    process.exit(1);
  }, 10000);
};

// Listen for termination signals
process.on('SIGTERM', () => gracefulShutdown('SIGTERM'));
process.on('SIGINT', () => gracefulShutdown('SIGINT'));

// Handle unhandled promise rejections
process.on('unhandledRejection', (err: Error) => {
  console.error('Unhandled Promise Rejection:', err.message);
  process.exit(1);
});

// Handle uncaught exceptions
process.on('uncaughtException', (err: Error) => {
  console.error('Uncaught Exception:', err.message);
  process.exit(1);
});

export default app;